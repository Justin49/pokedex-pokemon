<?php 

// Model Type

class TypeModel
{
    private $id;
    private $name;
    private $color;

    public function findsColor()
    {
        $sql = 'SELECT * FROM `type`';

        $pdo = Database::getPDO();

        $statement = $pdo->query($sql);

        $colors = $statement->fetchAll(PDO::FETCH_CLASS, 'TypeModel');

        return $colors;

    }

    /**
     * Get the value of id
     */ 
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */ 
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of name
     */ 
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set the value of name
     *
     * @return  self
     */ 
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get the value of color
     */ 
    public function getColor()
    {
        return $this->color;
    }

    /**
     * Set the value of color
     *
     * @return  self
     */ 
    public function setColor($color)
    {
        $this->color = $color;

        return $this;
    }
}